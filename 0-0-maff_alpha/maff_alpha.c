#include <unistd.h>

int	main(void)
{
	int odd;
	int even;

	odd = 'a';
	even = 'B';
	while (odd <= 'z')
	{
		write(1, &odd, 1);
		write(1, &even, 1);
		odd = odd + 2;
		even = even + 2;
	}
	write(1, "\n", 1);
	return (0);
}
