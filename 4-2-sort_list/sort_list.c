#include "list.h"

t_list	*sort_list(t_list *lst, int (*cmp)(int, int))
{
	t_list	*tmp_lst;
	int		swap;

	tmp_lst = lst;
	while (lst->next)
	{
		if ((*cmp)(lst->data, lst->next->data) == 0)
		{
			swap = lst->data;
			lst->data = lst->next->data;
			lst->next->data = swap;
			lst = tmp_lst;
		}
		else
			lst = lst->next;
	}
	lst = tmp_lst;
	return (lst);
}
